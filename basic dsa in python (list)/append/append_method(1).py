# creating a list named Elements
Elements = ["Hydrogen", "Helium", "Lithium", "Beryllium", "Boron" ]
# printing the list before and after append() method
print(f"Before append(): {Elements}")
# appending "Carbon" to the list
Elements.append("Carbon")
# printing the list after appending "Carbon"
print(f"After append():{Elements}")


# output:
# Before append(): ['Hydrogen', 'Helium', 'Lithium', 'Beryllium', 'Boron']
# After append():['Hydrogen', 'Helium', 'Lithium', 'Beryllium', 'Boron', 'Carbon']