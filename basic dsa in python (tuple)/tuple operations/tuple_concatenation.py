# create tuple of fruits
fruits = ("apple", "banana", "cherry", "date", "strawberry")

print(f"Fruits Tuple: {fruits}")

new_fruits = fruits + ("orange", "kiwi")

print(f"New Fruits Tuple: {new_fruits}")

# output:
# Fruits Tuple: ('apple', 'banana', 'cherry', 'date', 'strawberry')
# New Fruits Tuple: ('apple', 'banana', 'cherry', 'date', 'strawberry', 'orange', 'kiwi')