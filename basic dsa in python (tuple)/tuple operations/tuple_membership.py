# create tuple of fruits
fruits = ("apple", "banana", "cherry", "date", "strawberry")

if "apple" in fruits:
    print("Yes, 'apple' is in the fruits tuple")
else:
    print("No, 'apple' is not in the fruits tuple")

if "orange" in fruits:
    print("Yes, 'orange' is in the fruits tuple")
else:
    print("No, 'orange' is not in the fruits tuple")


# output:
# Yes, 'apple' is in the fruits tuple
# No, 'orange' is not in the fruits tuple
