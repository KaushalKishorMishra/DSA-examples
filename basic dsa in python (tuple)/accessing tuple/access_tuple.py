# accessing tuple with index
fruits = ("apple", "banana", "cherry", "date", "strawberry")

index_0_item = fruits[0]
index_1_item = fruits[1]
index_last_item = fruits[-1]

print(f"Fruits Tuple: {fruits}")
print(f"Item at index 0: {index_0_item}")
print(f"Item at index 1: {index_1_item}")
print(f"Item at last index: {index_last_item}")

# output:
# Fruits Tuple: ('apple', 'banana', 'cherry', 'date', 'strawberry')
# Item at index 0: apple
# Item at index 1: banana
# Item at last index: strawberry
